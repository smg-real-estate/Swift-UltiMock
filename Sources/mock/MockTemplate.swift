                    guard stub.matches(invocation) else {
                        handleFatalFailure(
                            "Unexpected call: expected `\\(stub.expectation)`, but received `\\(invocation)`",
                            fileID: stub.fileID,
                            filePath: stub.filePath,
                            line: stub.line,
                            column: stub.column
                        )
                    }

                    defer { recorder.checkVerification() }
                    return stub.perform
                }
