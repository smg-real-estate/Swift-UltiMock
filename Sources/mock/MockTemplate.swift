                .map { "\n" + $0.mockExpect(forwarding: mocksClass) }

            mockedTypeInfo.subscripts.map { "\n" + $0.mockExpectGetter }

            mockedTypeInfo.subscripts.map { "\n" + $0.mockExpectSetter }
