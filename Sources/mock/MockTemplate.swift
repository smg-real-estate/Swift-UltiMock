            let isObjc = objcClassNames.contains(type.name)

            mockedTypeInfo.methods
                .filter { method in
                    !isObjc || !method.method.isAsync
                }
                .map {
                    "\n" + $0.implementation(override: mocksClass)
                        .indented(1)
                }

            mockedTypeInfo.properties.map {
                "\n" + $0.implementation(override: mocksClass)
                    .indented(1)
            }

            mockedTypeInfo.subscripts.map {
                "\n" + $0.implementation
                    .indented(1)
            }

